    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String sa = this.aa.getText();
        double pa = Double.parseDouble(sa);
        String sb = this.bb.getText();
        double pb = Double.parseDouble(sb);
        String sc = this.cc.getText();
        double pc = Double.parseDouble(sc);
        Retta retta = new Retta(pa,pb,pc);
        String sx = this.px.getText();
        double x = Double.parseDouble(sx);
        String sy = this.py.getText();
        double y = Double.parseDouble(sy);
        Retta r = new Retta(pa,pb,pc);
        Boolean ver = r.appartiene( x , y);
        if(ver == true){
            this.app.setText("Il punto appartiene alla retta");
        }else{
            this.app.setText("Il punto NON appartiene alla retta");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

