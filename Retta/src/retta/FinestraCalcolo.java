    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sa = this.aa.getText();
        double pa = Double.parseDouble(sa);
        String sb = this.bb.getText();
        double pb = Double.parseDouble(sb);
        String sc = this.cc.getText();
        double pc = Double.parseDouble(sc);
        if((pa==0)&&(pb==0)){
            FinestraErrore f = new FinestraErrore();
            f.setVisible(true);
            return;
        }
        Retta retta = new Retta(pa,pb,pc);
        String tipo = retta.getTipo();
        String e = retta.stampa();
        this.t.setText(String.valueOf(tipo));
        this.eq.setText(String.valueOf(e));
    }//GEN-LAST:event_jButton1ActionPerformed

